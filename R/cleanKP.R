  dat$TeamName <- gsub("UMBC", "Maryland-Baltimore County", dat$TeamName)
  dat$TeamName <- gsub("Miami OH", "Miami (Ohio)", dat$TeamName)
  dat$TeamName <- gsub("Texas A&M Corpus Chris", "Texas A&M-Corpus Christi", dat$TeamName)
  dat$TeamName <- gsub("Central Connecticut", "Central Connecticut State", dat$TeamName)
  dat$TeamName <- gsub("UCF", "Central Florida", dat$TeamName)
  dat$TeamName <- gsub("Illinois Chicago", "Illinois-Chicago", dat$TeamName)
  dat$TeamName <- gsub("Troy State", "Troy", dat$TeamName)
  return(dat)
}
