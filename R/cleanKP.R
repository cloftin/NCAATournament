  dat$TeamName <- gsub("Arkansas Arkansas-Little Rock", "Arkansas-Little Rock", dat$TeamName)
  dat$TeamName <- gsub("BYU", "Brigham Young", dat$TeamName)
  dat$TeamName <- gsub("Albany", "Albany (N.Y.)", dat$TeamName)
  dat$TeamName <- gsub("Saint Mary's", "St. Mary's (Cal.)", dat$TeamName)
  dat$TeamName <- gsub("State John's", "St. John's", dat$TeamName)
  dat$TeamName <- gsub("UAB", "Alabama-Birmingham", dat$TeamName)
  dat$TeamName <- gsub("Louisiana Lafayette", "Louisiana-Lafayette", dat$TeamName)
  dat$TeamName <- gsub("Cal Poly", "Cal-Poly", dat$TeamName)
  dat$TeamName <- gsub("State Louis", "St. Louis", dat$TeamName)
  dat$TeamName <- gsub("Saint Louis", "St. Louis", dat$TeamName)
